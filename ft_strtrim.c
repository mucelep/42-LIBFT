#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	len;
	char	*rst;

	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	len = ft_strlen(s1);
	while (len && ft_strchr(set, s1[len - 1]))
		len--;
	rst = malloc(len + 1);
	if (!rst)
		return (NULL);
	ft_memcpy(rst, s1, len);
	rst[len] = '\0';
	return (rst);
}
